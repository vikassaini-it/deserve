  currentPosition: null,

  //--------------------------------------------------------------------------------------------------
  //												THUNKS
  //--------------------------------------------------------------------------------------------------

  //--------------------------------------------------------------------------------------------------
  //												ACTIONS
  //--------------------------------------------------------------------------------------------------

